int	ft_is_prime(int nb)
{
	int	i;

	i = 2;
	if (nb <= 1)
		return (0);
	while (i < nb && nb > 2)
	{
		if (nb % 2 == 2)
			return (0);
		else if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
